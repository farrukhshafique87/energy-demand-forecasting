"""
run_comparison.py — runs naive baseline, SARIMA, and XGBoost on the same
chronological holdout and reports an honest comparison table.

Key design decisions:
- All models evaluated on the same time period (the 20% chronological holdout
  from features_df, which has a different start than raw_df due to lag warm-up)
- Naive baselines generated by index-lookup into raw_df (shift by 1 week),
  then aligned to the holdout index — avoids positional mismatch between
  raw_df and features_df which have different lengths
- SARIMA evaluated on 48h only (its intended operating range); XGBoost at
  both full holdout and 48h for direct comparison
- Timezone stripped from all indices before alignment to avoid tz-aware vs
  tz-naive comparison errors

Usage:
    python eval/run_comparison.py
"""

import sys
from pathlib import Path
sys.path.insert(0, str(Path(__file__).parent.parent / "src"))

import numpy as np
import pandas as pd
from sklearn.metrics import mean_absolute_error, mean_squared_error

from model_sarima import forecast_sarima
from model_ml import train_xgboost, forecast_xgboost, TARGET_COL

PROCESSED_DIR = Path(__file__).parent.parent / "data" / "processed"
STEPS_PER_WEEK = 96 * 7   # 15-min resolution
SARIMA_HOURS   = 48
STEPS_48H      = SARIMA_HOURS * 4   # 192 steps


def strip_tz(index: pd.DatetimeIndex) -> pd.DatetimeIndex:
    """Remove timezone info if present — makes index intersection work
    regardless of whether data came from the API (tz-aware) or CSV (tz-naive)."""
    if index.tz is not None:
        return index.tz_localize(None)
    return index


def mape(y_true: np.ndarray, y_pred: np.ndarray) -> float:
    mask = y_true != 0
    return float(np.mean(np.abs(
        (y_true[mask] - y_pred[mask]) / y_true[mask]
    )) * 100)


def score(name: str, horizon: str,
          y_true: np.ndarray, y_pred: np.ndarray,
          naive_mae: float = None) -> dict:
    mae  = mean_absolute_error(y_true, y_pred)
    rmse = float(np.sqrt(mean_squared_error(y_true, y_pred)))
    skill = round(1 - mae / naive_mae, 3) if naive_mae else "—"
    return {
        "model":          name,
        "MAE (MW)":       round(mae,  1),
        "RMSE (MW)":      round(rmse, 1),
        "MAPE (%)":       round(mape(y_true, y_pred), 2),
        "skill_vs_naive": skill,
        "horizon":        horizon,
        "n_points":       len(y_true),
    }


def naive_preds_for_period(raw: pd.Series,
                           test_index: pd.DatetimeIndex,
                           lag_steps: int) -> np.ndarray:
    """Generate naive predictions by looking up raw[t - lag] for each t
    in test_index. Uses index alignment so date ranges always match,
    regardless of length differences between raw and features DataFrames."""
    raw = raw.copy()
    raw.index = strip_tz(raw.index)
    shifted = raw.shift(lag_steps)           # value at t is raw[t - lag_steps]
    test_index = strip_tz(test_index)
    preds = shifted.reindex(test_index)      # pick only the holdout timestamps
    n_missing = preds.isna().sum()
    if n_missing > 0:
        print(f"  Note: {n_missing} NaN in naive preds (no history {lag_steps} "
              f"steps before some timestamps). Filling with series mean.")
        preds = preds.fillna(raw.mean())
    return preds.values


def main():
    features_path = PROCESSED_DIR / "features.parquet"
    raw_path      = PROCESSED_DIR / "load_germany.parquet"

    if not features_path.exists() or not raw_path.exists():
        print("Missing processed data. Run:")
        print("  python src/data_loader.py --source api --start 2023-01-01 --end 2024-12-31")
        print("  python src/features.py")
        return

    raw_df      = pd.read_parquet(raw_path)
    features_df = pd.read_parquet(features_path)

    # strip tz from both so all index ops are tz-naive
    raw_df.index      = strip_tz(raw_df.index)
    features_df.index = strip_tz(features_df.index)

    # chronological 80/20 split on the feature set
    split_idx = int(len(features_df) * 0.8)
    train_df  = features_df.iloc[:split_idx]
    test_df   = features_df.iloc[split_idx:]

    # raw series up to the train cutoff (for SARIMA)
    cutoff    = train_df.index[-1]
    train_raw = raw_df["load_mw"].loc[:cutoff]

    # actual values for the holdout — as numpy (no index needed after alignment)
    y_full = test_df[TARGET_COL].values
    y_48h  = test_df[TARGET_COL].iloc[:STEPS_48H].values
    test_index      = test_df.index
    test_index_48h  = test_df.index[:STEPS_48H]

    results = []

    # ── 1. Seasonal naive (t-1 week) ─────────────────────────────────────────
    print("Running seasonal naive baseline...")
    sn_full = naive_preds_for_period(raw_df["load_mw"], test_index, STEPS_PER_WEEK)
    naive_mae_full = mean_absolute_error(y_full, sn_full)
    results.append(score("Seasonal Naive (t-1 week)", "full holdout",
                         y_full, sn_full))

    # ── 2. Last-value naive (t-1 step) ───────────────────────────────────────
    print("Running last-value naive baseline...")
    lv_full = naive_preds_for_period(raw_df["load_mw"], test_index, 1)
    results.append(score("Naive (last value)", "full holdout",
                         y_full, lv_full, naive_mae_full))

    # ── 3. SARIMA — 48h horizon ───────────────────────────────────────────────
    print(f"Fitting SARIMA (48h horizon)...")
    try:
        sarima_hourly = forecast_sarima(train_raw, horizon_steps=SARIMA_HOURS)
        sarima_vals   = sarima_hourly.values   # 48 values

        # downsample 48h actuals to hourly (mean of each 4-step block)
        y_48h_hourly = y_48h.reshape(SARIMA_HOURS, 4).mean(axis=1)
        sn_48h       = naive_preds_for_period(
            raw_df["load_mw"], test_index_48h, STEPS_PER_WEEK
        )
        sn_48h_hourly    = sn_48h.reshape(SARIMA_HOURS, 4).mean(axis=1)
        naive_mae_48h_hr = mean_absolute_error(y_48h_hourly, sn_48h_hourly)

        results.append(score(f"SARIMA ({SARIMA_HOURS}h horizon)", f"{SARIMA_HOURS}h",
                             y_48h_hourly, sarima_vals, naive_mae_48h_hr))
    except Exception as e:
        print(f"  SARIMA failed: {e}")

    # ── 4. XGBoost ───────────────────────────────────────────────────────────
    print("Training XGBoost...")
    xgb_model = train_xgboost(train_df)

    xgb_full = forecast_xgboost(xgb_model, test_df).values
    results.append(score("XGBoost (full holdout)", "full holdout",
                         y_full, xgb_full, naive_mae_full))

    xgb_48h = forecast_xgboost(xgb_model, test_df.iloc[:STEPS_48H]).values
    sn_48h_preds     = naive_preds_for_period(
        raw_df["load_mw"], test_index_48h, STEPS_PER_WEEK
    )
    naive_mae_48h    = mean_absolute_error(y_48h, sn_48h_preds)
    results.append(score(f"XGBoost ({SARIMA_HOURS}h horizon)", f"{SARIMA_HOURS}h",
                         y_48h, xgb_48h, naive_mae_48h))

    # ── Report ────────────────────────────────────────────────────────────────
    df_out = pd.DataFrame(results)
    sep = "=" * 88
    print(f"\n{sep}")
    print("FORECAST COMPARISON — chronological holdout, no leakage")
    print(f"SARIMA: 48h horizon only. XGBoost: full holdout + 48h.")
    print(sep)
    print(df_out.to_string(index=False))
    print(sep)
    print("\nskill_vs_naive:  0 = matches naive  |  1.0 = perfect  |  "
          "negative = worse than naive")

    out_path = Path(__file__).parent / "comparison_results.csv"
    df_out.to_csv(out_path, index=False)
    print(f"\nSaved to {out_path}")


if __name__ == "__main__":
    main()
